# 예제 7: 어떤 초등학교에서 10년간 조사한 결과,
# 평균적으로 전체 학생의 4%가 색맹인 것으로 나타났다. (p.213)

# 올해 학생 중에서도 색맹 비율이 예년과 동일하다고 가정할 때,
# 전체 학생 200명 중 색맹인 학생이 10명 이하일 확률을 계산한다.

from scipy import stats

# 이항분포를 이용한 계산:
# n=200 (학생 수), p=0.04 (색맹일 확률), P(X ≤ 10)
binom_prob = stats.binom.cdf(10, 200, 0.04)
print(binom_prob)

# 결과 해석:
# 색맹 학생이 10명 이하일 확률은 약 0.820 (82.0%)이다.


# 포아송 근사를 이용한 계산:
# λ = np = 200 * 0.04 = 8
# λ = 8일 때, P(X ≤ 10)
poisson_prob = stats.poisson.cdf(10, 8)
print(poisson_prob)

# 결과 해석:
# 포아송 근사를 사용하면 확률은 약 0.816 (81.6%)로 계산된다.


# 결론:
# 실제 이항분포로 계산한 결과와 포아송 근사 결과가 유사하게 나왔으며,
# 이는 n이 크고 p가 작을 때 포아송 분포가 이항분포를 잘 근사함을 보여준다.
